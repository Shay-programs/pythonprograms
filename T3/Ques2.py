'''Create a list of size 5 and execute the slicing structure '''

list1 = [10,20,30,40,50]

# reverse list
print ('Reverse List', list1[::-1])

# slicing elemnets of index 1 to 3 
print(list1[1:4])

#slicing alternate elements
print(list1[::2])

#get 1st element
print(list1[:1])

#get last element
print(list1[4:])